const { contextBridge, ipcRenderer } = require('electron');

contextBridge.exposeInMainWorld('miniEditor', {
  openFile: () => ipcRenderer.invoke('file-open'),
  openFilePath: (filePath) => ipcRenderer.invoke('file-open-path', filePath),
  pickFolder: () => ipcRenderer.invoke('pick-folder'),
  readFolderTree: (folderPath) => ipcRenderer.invoke('read-folder-tree', folderPath),
  checkResearchFiles: (payload) => ipcRenderer.invoke('research-check-files', payload),
  getTypstOutline: (payload) => ipcRenderer.invoke('typst-outline', payload),
  saveFile: (payload) => ipcRenderer.invoke('file-save', payload),
  showPreviewMenu: () => ipcRenderer.send('preview-context-menu'),
  exportPdf: (payload) => ipcRenderer.invoke('export-pdf', payload),
  exportTypstPdf: (payload) => ipcRenderer.invoke('typst-export-pdf', payload),
  printPreview: (payload) => ipcRenderer.invoke('print-preview', payload),
  renderTypstPreview: (payload) => ipcRenderer.invoke('typst-preview-render', payload),
  writeAutosave: (payload) => ipcRenderer.invoke('autosave-write', payload),
  readAutosave: () => ipcRenderer.invoke('autosave-read'),
  clearAutosave: () => ipcRenderer.invoke('autosave-clear'),
  confirmClose: (payload) => ipcRenderer.invoke('confirm-close', payload),
  readMenuState: () => ipcRenderer.invoke('menu-state-read'),
  writeMenuState: (state) => ipcRenderer.invoke('menu-state-write', state),
  closeWindow: () => ipcRenderer.invoke('close-window'),
  setTitle: (title) => ipcRenderer.invoke('set-title', title),
  showContextMenu: (payload) => ipcRenderer.send('show-context-menu', payload),
  onPreviewExportPdf: (callback) => ipcRenderer.on('preview-export-pdf', callback),
  onBeforeClose: (callback) => ipcRenderer.on('app-before-close', callback),
  onMenuNew: (callback) => ipcRenderer.on('menu-new', callback),
  onMenuOpen: (callback) => ipcRenderer.on('menu-open', callback),
  onMenuSave: (callback) => ipcRenderer.on('menu-save', callback),
  onMenuSaveAs: (callback) => ipcRenderer.on('menu-save-as', callback),
  onMenuExportPdf: (callback) => ipcRenderer.on('menu-export-pdf', callback),
  onMenuPrint: (callback) => ipcRenderer.on('menu-print', callback),
  onMenuFind: (callback) => ipcRenderer.on('menu-find', callback),
  onMenuOpenFolder: (callback) => ipcRenderer.on('menu-open-folder', callback),
  onMenuFindNext: (callback) => ipcRenderer.on('menu-find-next', callback),
  onMenuFindPrevious: (callback) => ipcRenderer.on('menu-find-previous', callback),
  onMenuReplace: (callback) => ipcRenderer.on('menu-replace', callback),
  onMenuTimeDate: (callback) => ipcRenderer.on('menu-time-date', callback),
  onMenuWordWrap: (callback) => ipcRenderer.on('menu-word-wrap', callback),
  onMenuStatusBar: (callback) => ipcRenderer.on('menu-status-bar', callback),
  onMenuFolderTree: (callback) => ipcRenderer.on('menu-folder-tree', callback),
  onMenuLineNumbers: (callback) => ipcRenderer.on('menu-line-numbers', callback),
  onMenuDocumentMode: (callback) => ipcRenderer.on('menu-document-mode', callback),
  onMenuTheme: (callback) => ipcRenderer.on('menu-theme', callback),
  onMenuTogglePreview: (callback) => ipcRenderer.on('menu-toggle-preview', callback),
  onMenuFont: (callback) => ipcRenderer.on('menu-font', callback),
  onMenuFontSize: (callback) => ipcRenderer.on('menu-font-size', callback),
  onContextOpenTreeFile: (callback) => ipcRenderer.on('context-open-tree-file', callback),
  onContextRefreshFolderTree: (callback) => ipcRenderer.on('context-refresh-folder-tree', callback)
});
